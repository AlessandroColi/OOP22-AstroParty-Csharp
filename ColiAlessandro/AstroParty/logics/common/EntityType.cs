namespace AstroParty
{
    /// <summary>
    /// An enum of the possible types of PowerUp inside AstroParty.
    /// </summary>
    public enum EntityType
    {
        /// <summary>
        /// A spaceship.
        /// </summary>
        SPACESHIP,

        /// <summary>
        /// A general type of powerUP.
        /// </summary>
        POWERUP,

        /// <summary>
        /// The shield powerUP.
        /// </summary>
        SHIELD ,

        /// <summary>
        /// The immortality powerUP.
        /// </summary>
        IMMORTALITY,

        /// <summary>
        /// The doubleshot powerUP.
        /// </summary>
        DOUBLESHOT,

        /// <summary>
        /// The UpgradedSpeed powerUP.
        /// </summary>
        UPGRADEDSPEED,

        /// <summary>
        /// A general obstacle.
        /// </summary>
        OBSTACLE,

        /// <summary>
        /// A laser Obstacle.
        /// </summary>
        LASER,

        /// <summary>
        /// A simple Obstacle.
        /// </summary>
        SIMPLEOBSTACLE,

        /// <summary>
        /// An undestroyable Obstacle.
        /// </summary>
        UNDESTROYABLEOBSTACLE,

        /// <summary>
        /// A projectile.
        /// </summary>
        PROJECTILE
    }
}